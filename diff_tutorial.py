#**Comando `git diff`**
# O comando `git diff` é usado para mostrar as diferenças entre o que foi modificado no seu repositório.
# Ele pode ser utilizado de várias maneiras para comparar mudanças no stage, commits ou branches.

# Resumo das Diferenças:
# - **Commits**: Representam o histórico de mudanças feitas no repositório.
# - **Stage**: Área onde você prepara as mudanças antes de commitá-las.
# - **Branches**: Permitem que você crie diferentes versões do seu projeto para desenvolver novas funcionalidades ou
# testar sem afetar o branch principal.
# - **git diff**: Comando que permite comparar alterações no código, seja entre arquivos não comitados,
# entre o stage e os commits, ou entre branches.
